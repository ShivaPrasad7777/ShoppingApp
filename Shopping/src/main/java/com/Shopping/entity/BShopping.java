package com.Shopping.entity;

public class BShopping {
	String bname;
	String bmail;
	String bpwd;
	int bmnum;
	public String getBname() {
		return bname;
	}
	public void setBname(String bname) {
		this.bname = bname;
	}
	public String getBmail() {
		return bmail;
	}
	public void setBmail(String bmail) {
		this.bmail = bmail;
	}
	public String getBpwd() {
		return bpwd;
	}
	public void setBpwd(String bpwd) {
		this.bpwd = bpwd;
	}
	public int getBmnum() {
		return bmnum;
	}
	public void setBmnum(int bmnum) {
		this.bmnum = bmnum;
	}
	

}
